    private void btnmoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmoiActionPerformed
        if (btnthem.isEnabled()) {
            btnthem.setEnabled(false);
            btnsua.setEnabled(true);
            btnxoa.setEnabled(true);
            if (getNVlog().getChucvu() == 0) {
                btnxoa.setEnabled(false);
            }
            settext();
        } else {
            btnthem.setEnabled(true);
            btnsua.setEnabled(false);
            btnxoa.setEnabled(false);
            refresh();
        }
    }//GEN-LAST:event_btnmoiActionPerformed

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        Nhanvien x = new Nhanvien();
        x.setId(txtmaNv.getText());
        x.setName(txtname.getText());
        x.setMatkhau(txtpw.getText());
        x.setChucvu(rdnv.isSelected() == true ? 0 : 1);
        String tb = addNV(x);
        if (tb.equals("") == false) {
            JOptionPane.showMessageDialog(rootPane, tb);
        }
        if (tb.equals("OK")) {
            nhanvienDAO.insertNV(x);
        }
        loadTb();
        refresh();
    }//GEN-LAST:event_btnthemActionPerformed

