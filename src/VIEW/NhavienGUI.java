    private void loadTb() {
        tbModel.setRowCount(0);
        int i = 1;
        for (Nhanvien x : nhanvienDAO.lstLog()) {
            tbModel.addRow(new Object[]{
                i++,
                x.getId(),
                x.getMatkhau(),
                x.getName(),
                x.getChucvu() == 0 ? "Nhân viên" : "Trưởng phòng"
            });
        }
    }

    private String addNV(Nhanvien x) {
        if (x.getId().isBlank()) {
            return "Mã nhân viên không bỏ trống";
        }
        if (Help.Checkloi.checkmaNV(x.getId(), nhanvienDAO.lstLog()) > -1) {
            return "Mã nhân viên đã tồn tại";
        }
        if (x.getName().isBlank()) {
            return "Họ và tên không bỏ trống";
        }
        if (x.getMatkhau().isBlank()) {
            return "Mật khẩu không bỏ trống";
        }
        if (txtpw2.getText().equals(x.getMatkhau()) == false) {
            return "Nhập lại đúng mật khẩu mới";
        }
        if (JOptionPane.showConfirmDialog(rootPane, "Chắc chắn thêm không?", "Thông báo",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            _lstadd.add(x);
            return "OK";
        }
        return "";
    }

    private void settext() {
        Nhanvien x = new Nhanvien();
        x = nhanvienDAO.lstLog().get(_index);
        txtmaNv.setText(x.getId());
        txtname.setText(x.getName());
        txtpw.setText(x.getMatkhau());
        txtpw2.setText(x.getMatkhau());
        rdnv.setSelected(x.getChucvu() == 0);
        rdtt.setSelected(x.getChucvu() == 1);
    }

    private String suaNV() {
        Nhanvien x = new Nhanvien();
        List<Nhanvien> lsttepm = nhanvienDAO.lstLog();
        x = lsttepm.get(Help.Checkloi.checkmaNV(txtmaNv.getText(), lsttepm));
        x.setId(txtmaNv.getText());
        x.setName(txtname.getText());
        x.setMatkhau(txtpw.getText());
        x.setChucvu(rdnv.isSelected() == true ? 0 : 1);
        if (x.getName().isBlank()) {
            return "Họ và tên không bỏ trống";
        }
        if (x.getMatkhau().isBlank()) {
            return "Mật khẩu không bỏ trống";
        }
        if (txtpw2.getText().equals(x.getMatkhau()) == false) {
            return "Nhập lại đúng mật khẩu mới";
        }
        if (JOptionPane.showConfirmDialog(rootPane, "Chắc chắn sửa không?", "Thông báo",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            _lstadd.add(x);
            nhanvienDAO.updateNV(x);
            return "OK";
        }
        return "";
    }

    private void refresh() {
        txtmaNv.setText("");
        txtpw.setText("");
        txtpw2.setText("");
        txtname.setText("");
        rdnv.setSelected(true);
    }

